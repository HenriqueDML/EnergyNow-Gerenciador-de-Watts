//1. Relatório utilizando classificação de dados
//Relatório de Diagnósticos por Cliente
// Ele deve retornar o nome do cliente junto a diagnosticos associados a ele
SELECT t_cliente.nome_cliente, COUNT(t_diagnostico.id_diagnostico) AS total_diagnosticos
FROM t_cliente
JOIN t_veiculo ON t_cliente.id_cliente = t_veiculo.id_cliente
JOIN t_diagnostico ON t_veiculo.id_veiculo = t_diagnostico.id_veiculo
GROUP BY t_cliente.nome_cliente
ORDER BY total_diagnosticos DESC;

//Relatório de Orçamentos por Veículo
//Retorna a contagem de modelos do mesmo tipo
SELECT t_veiculo.modelo, COUNT(t_orcamento.id_orcamento) AS total_orcamentos
FROM t_veiculo
JOIN t_orcamento ON t_veiculo.id_veiculo = t_orcamento.id_veiculo
GROUP BY t_veiculo.modelo
ORDER BY total_orcamentos DESC;



//2. Relatório utilizando alguma função do tipo numérica simples
//Valor Total de Orçamentos
//Rertorna o valor total dos orçamentos
SELECT SUM(valor) AS valor_total_orcamentos
FROM t_orcamento;

//Média de Valores de Peças
//Retorna a média do valor das peças
SELECT AVG(valor_peca) AS media_valor_pecas
FROM t_pecas;



//3. Relatório utilizando alguma função de grupo
//Contagem de Clientes por Estado
//Este comando mostra quantos clientes temos em cada estado
SELECT t_endereco.estado, COUNT(t_cliente.id_cliente) AS total_clientes
FROM t_cliente
JOIN t_endereco ON t_cliente.id_cliente = t_endereco.id_cliente
GROUP BY t_endereco.estado;

//Total de Peças por Diagnóstico
//Aqui é possivel ver quantas peças estão em cada diagnóstico
SELECT t_diagnostico.perguntas, COUNT(t_pecas.id_pecas) AS total_pecas
FROM t_diagnostico
JOIN t_pecas ON t_diagnostico.id_diagnostico = t_pecas.id_diagnostico
GROUP BY t_diagnostico.perguntas;



//4. Relatório utilizando subconsulta
//Clientes com Orçamento Acima da Média
// Aqui é possivel verificar clientes acima da média de orçamento
SELECT t_cliente.nome_cliente
FROM t_cliente
WHERE t_cliente.id_cliente IN (
    SELECT t_veiculo.id_cliente
    FROM t_veiculo
    JOIN t_orcamento ON t_veiculo.id_veiculo = t_orcamento.id_veiculo
    WHERE t_orcamento.valor > (
        SELECT AVG(valor) FROM t_orcamento
    )
);

//Veículos que Tiveram Diagnósticos Específicos
// Esse trecho podemos verificar carros que tiveram 'motor' em seu diagnostico
SELECT t_veiculo.modelo
FROM t_veiculo
WHERE t_veiculo.id_veiculo IN (
    SELECT t_diagnostico.id_veiculo
    FROM t_diagnostico
    WHERE t_diagnostico.perguntas LIKE '%motor%'
);



//5. Relatório utilizando junção de tabelas
//Orçamentos com Informações do Cliente e Veículo
//Esse codigo traz informações sobre os orçamentos.
SELECT t_cliente.nome_cliente, t_veiculo.modelo, t_orcamento.valor, t_orcamento.datao
FROM t_orcamento
JOIN t_veiculo ON t_orcamento.id_veiculo = t_veiculo.id_veiculo
JOIN t_cliente ON t_veiculo.id_cliente = t_cliente.id_cliente;


