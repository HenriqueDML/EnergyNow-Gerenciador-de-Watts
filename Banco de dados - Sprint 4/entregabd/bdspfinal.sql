CREATE TABLE t_cliente (
   id_cliente INTEGER GENERATED BY DEFAULT AS IDENTITY,
   nome_cliente VARCHAR(50)NOT NULL,
   email_cliente VARCHAR(50)NOT NULL,
   senha_cliente VARCHAR(30)NOT NULL,
   cpf_cnpj_cliente VARCHAR(11) NOT NULL,
   CONSTRAINT t_cliente_chave_primaria PRIMARY KEY (id_cliente),
   CONSTRAINT t_cliente_cpf_unico UNIQUE (cpf_cnpj_cliente)
);

----------------------------------------------------------------------------------------------------
CREATE TABLE t_endereco (
   id_endereco INTEGER GENERATED BY DEFAULT AS IDENTITY,
   rua VARCHAR(50)NOT NULL,
   cep VARCHAR(8)NOT NULL,
   bairro VARCHAR(50)NOT NULL,
   cidade VARCHAR(50)NOT NULL,
   regiao VARCHAR(50)NOT NULL,
   estado CHAR(2)NOT NULL,
   complemento VARCHAR(50)NOT NULL,
   numero VARCHAR(50)NOT NULL,
   id_cliente INTEGER NOT NULL,
   id_oficina INTEGER NOT NULL,
   CONSTRAINT t_endereco_t_cliente_fk FOREIGN KEY (id_cliente)REFERENCES t_cliente(id_cliente),
   CONSTRAINT t_endereco_t_oficina_fk FOREIGN KEY (id_oficina)REFERENCES t_oficina(id_oficina),
   CONSTRAINT t_endereco_chave_primaria PRIMARY KEY (id_endereco)
);

-----------------------------------------------------------------------------------------------------------

CREATE TABLE t_telefone (
   id_telefone INTEGER GENERATED BY DEFAULT AS IDENTITY,
   ddd VARCHAR(50)NOT NULL,
   numero VARCHAR(50)NOT NULL,
   id_cliente INTEGER NOT NULL,
   id_oficina INTEGER NOT NULL,
   CONSTRAINT t_telefone_t_cliente_fk FOREIGN KEY (id_cliente)REFERENCES t_cliente(id_cliente),
   CONSTRAINT t_telefone_t_oficina_fk FOREIGN KEY (id_oficina)REFERENCES t_oficina(id_oficina),
   CONSTRAINT t_telefone_chave_primaria PRIMARY KEY (id_telefone)
);

--------------------------------------------------------------------------------------------------------
CREATE TABLE t_veiculo (
   id_veiculo INTEGER GENERATED BY DEFAULT AS IDENTITY,
   placa VARCHAR(8)NOT NULL,
   modelo VARCHAR(15)NOT NULL,
   marca VARCHAR(15)NOT NULL,
   id_cliente INTEGER NOT NULL,
   CONSTRAINT t_veiculo_t_cliente_fk FOREIGN KEY (id_cliente)REFERENCES t_cliente(id_cliente),
   CONSTRAINT t_veiculo_chave_primaria PRIMARY KEY (id_veiculo)
);

----------------------------------------------------------------------------------------------------
CREATE TABLE t_diagnostico (
   id_diagnostico INTEGER GENERATED BY DEFAULT AS IDENTITY,
   perguntas VARCHAR(200)NOT NULL,
   respostas VARCHAR(200)NOT NULL,
   sugestoes VARCHAR(300)NOT NULL,
   id_veiculo INTEGER NOT NULL,
   CONSTRAINT t_diagnostico_t_veiculo_fk FOREIGN KEY (id_veiculo)REFERENCES t_veiculo(id_veiculo),
   CONSTRAINT t_diagnostico_chave_primaria PRIMARY KEY (id_diagnostico)
);

----------------------------------------------------------------------------------------------------
CREATE TABLE t_pecas (
   id_pecas INTEGER GENERATED BY DEFAULT AS IDENTITY,
   quantidade VARCHAR(200) NOT NULL,
   nome_da_peca VARCHAR(30) NOT NULL,
   funcao_peca VARCHAR(300) NOT NULL,
   valor_peca DECIMAL(8,2) NOT NULL,
   id_diagnostico INTEGER NOT NULL,
   CONSTRAINT t_pecas_chave_primaria PRIMARY KEY (id_pecas),
   CONSTRAINT t_pecas_t_diagnostico_fk FOREIGN KEY (id_diagnostico)REFERENCES t_diagnostico(id_diagnostico),
   CONSTRAINT t_diagnostico_valor_peca_ck CHECK (valor_peca > 0)
);

------------------------------------------------------------------------------------------------------------
CREATE TABLE t_historico (
   id_historico INTEGER GENERATED BY DEFAULT AS IDENTITY,
   sugestoes VARCHAR(200) NOT NULL,
   registros VARCHAR(200) NOT NULL,
   perguntas VARCHAR(200) NOT NULL,
   respostas VARCHAR(200) NOT NULL,
      id_cliente INTEGER NOT NULL,
   CONSTRAINT t_historico_chave_primaria PRIMARY KEY (id_historico),
   CONSTRAINT t_historico_t_cliente_fk FOREIGN KEY (id_cliente)REFERENCES t_cliente(id_cliente)
);

-------------------------------------------------------------------------------------------------------------
CREATE TABLE t_orcamento (
   id_orcamento INTEGER GENERATED BY DEFAULT AS IDENTITY,
   datao DATE,
   valor DECIMAL(8,2),
   id_veiculo INTEGER NOT NULL,
   CONSTRAINT t_orcamento_chave_primaria PRIMARY KEY (id_orcamento),
   CONSTRAINT t_orcamento_t_veiculo_fk FOREIGN KEY (id_veiculo)REFERENCES t_veiculo(id_veiculo),
   CONSTRAINT t_orcamento_valor_ck CHECK (valor > 0)
);

